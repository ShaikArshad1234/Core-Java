
package patterns;

public class Space {
	public static void main(String[] args) {
		int n=9;
		for(int i=1;i<=n/2+1;i++) {
			for(int j=1;j<=i;j++) {
					System.out.print("*");
			if(j==2||j==4||j==6||j==8)
				
				System.out.print("-");
			}
				
		
			
	
		System.out.println();
		}
		
	
}
}
